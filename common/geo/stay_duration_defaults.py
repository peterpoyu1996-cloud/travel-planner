def estimate_default_stay_minutes(entry: dict) -> float | None:
    """entry 沒有可解析的 suggested_stay_duration 時，用名稱關鍵字猜一個類別預設值。
    category=hotel 不適用（過夜住宿不是行程中途停留點），回傳 None。
    比對不到任何關鍵字也回傳 None，呼叫端自己 fallback 到通用預設值。
    """
    if entry.get("category") == "hotel":
        return None

    haystack = f"{entry.get('name') or ''} {entry.get('name_local') or ''}".lower()
    for _label, keywords, minutes in _CATEGORY_RULES:
        if any(kw.lower() in haystack for kw in keywords):
            return minutes
    return None
