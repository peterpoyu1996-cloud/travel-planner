            # 沖繩幾間指標大型購物中心的專有名稱片段，不含在上面這些通用字裡
            # （例：那覇メインプレイス、DFSギャラリア沖縄都不含「モール」）
            "パルコ", "parco", "メインプレイス", "ギャラリア", "くもじ", "iias", "イーアス",
